/* --------------------------------------------- PRIVATE FUNCTION DEFINITIONS */

static uint8_t _strlen(uint8_t *s)
{
    uint8_t *sc;
    for (sc = s; *sc != '\0'; ++sc);
    return (sc - s);
}

static void _speed()
{
    switch(_speedScroll)
    {
        case 1: 
           Delay_100ms();
           Delay_100ms();
           break;
        case 2:
           Delay_100ms();
           break;
        case 3:
            Delay_10ms();
            Delay_10ms();
            Delay_10ms();
            break;
        default:
            Delay_100ms();
            break;
    }
}

