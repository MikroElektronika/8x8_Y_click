/* ----------------------------------------------------------- IMPLEMENTATION */

void c8x8y_writeCmd(uint8_t command, uint8_t _data)
{
    hal_gpio_csSet( 0 );
    hal_spiWrite(&command,1);
    hal_spiWrite(&_data,1);
    hal_gpio_csSet( 1 );
}

void c8x8y_displayRefresh()
{
   uint8_t cnt;
   
   for( cnt = 1; cnt < 9; cnt++)
   {
        c8x8y_writeCmd(cnt,0x00);
   }
   Delay_100ms();
}

void c8x8y_setSpeedScroll(uint8_t speed)
{
    _speedScroll = speed;
}

void c8x8y_displayString(char *pArray)
{
    uint8_t str_word[512];
    char wrChr;
    uint8_t cnt, i;
    uint8_t global_cnt = 0;
    uint8_t charAscii;
    uint8_t position = 0;
    uint8_t col = 8;
        
    for (i = 0; i < _strlen(pArray); i++)
    {
        charAscii = pArray[i] - 32;
        for( cnt = 0; cnt < 10; cnt++)
        {
            str_word[global_cnt++] = ascii_matrix[charAscii][cnt];
        }
    }
    
    while ( position < (global_cnt - 8) )
    {
         for( cnt = position; cnt < position + 8; cnt++)
         {
             wrChr = str_word[cnt];
             c8x8y_writeCmd(col, wrChr);
             col--;
         }
         col = 8;
         position++;
         _speed();
     }

}

void c8x8y_displayByte(char ch)
{
    uint8_t cnt;
    uint8_t charAscii;
    uint8_t position = 8;
    char wrChr;

    charAscii = ch - 32;
    for( cnt = 2; cnt < 10; cnt++)
    {
        wrChr = ascii_matrix[charAscii][cnt];
        c8x8y_writeCmd(position,wrChr);
        position--;
    }
}

void c8x8y_displayImage(uint8_t *pImage)
{
    uint8_t cnt;
    uint8_t line;
    uint8_t position = 8;
    
    for(cnt = 0; cnt < 8; cnt++)
    {
        line = pImage[cnt];
        c8x8y_writeCmd(position,line);
        position--;
    }
}





